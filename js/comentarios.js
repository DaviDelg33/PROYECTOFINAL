const main = document.querySelector(".usuario");
let info;

function usuarios() {
    fetch("https://randomuser.me/api/?results=5")
            .then(response => response.json())
            .then(resultado => {
                info = resultado;
                mostrar();
            })
            .catch(error => {
                main.innerHTML = `<p class='error'>${error}</p>`;
            });
}

function mostrar() {
    let usersHTML = "";
    
    for (let i = 0; i < 5; i++) {
        const comentarioAleatorio = comentarios[Math.floor(Math.random() *  comentarios.length)];
        let foto = info.results[i].picture.thumbnail;
        let nombre = info.results[i].name.first + " " + info.results[i].name.last;
        usersHTML +=
                `<div class="usu">` + `<div class="encabe">`+
                `<figure>` + `<img src='${foto}' alt='foto'>` + `</figure>` +
                `<p class="nombre">` + `${nombre}` + `</p>`+ `</div>` + `<p class="comentario">${comentarioAleatorio}</p>` +
                `</div>`;
    }
    main.innerHTML = usersHTML;

}
usuarios();
setInterval(() => {
    usuarios();
}, 8000);